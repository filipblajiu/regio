function whatTime(stops){
    let time = new Date();
    let h = time.getHours();
    let m = time.getMinutes();
    for (let s in stops) {
        if(h < stops[s][0]){
            return s;
        } else if( h == stops[s][0] && m < stops[s][1]){
            return s;
        }
    }
}


